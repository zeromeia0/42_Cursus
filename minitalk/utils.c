#include "minitalk.h"

void	ft_putstr(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		write(1, &str[i], 1);
		i++;
	}
}

void print_pid(void)
{
	pid_t process;
	process = getpid();
	printf("PID: %d\n", process);
}